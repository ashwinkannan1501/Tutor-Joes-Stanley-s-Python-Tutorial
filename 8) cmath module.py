# cmath module
import cmath

# Functions
print("--------- FUNCTIONS -----------")

print("sqrt(x) function :- ", cmath.sqrt(4))
print("rect(x, y) function :- ", cmath.rect(5.36, 8.963))

print("sin(x) function :- ", cmath.sin(20))
print("cos(x) function :- ", cmath.cos(15))
print("tan(x) function :- ", cmath.tan(30))

print("asin(x) function :- ", cmath.asin(15.3))
print("acos(x) function :- ", cmath.acos(12.6))
print("atan(x) function :- ", cmath.atan(10))

print("asinh(x) function :- ", cmath.asinh(52))
print("acosh(x) function :- ", cmath.acosh(10.3))
print("atanh(x) function :- ", cmath.atanh(85))

print("log(x, base=cmath.e) function :- ", cmath.log(12, 2))
print("log10(x) function :- ", cmath.log10(15))

print("polar(z) function :- ", cmath.polar(5 + 10j))
print("phase(z) function :- ", cmath.phase(2 + 3j))
print("exp(x) function :- ", cmath.exp(12))

print("isclose(x, y) function :- ", cmath.isclose(12.36, 18.96))
print("isfinite(x) function :- ", cmath.isfinite(15))
print("isinf(x) function :- ", cmath.isinf(cmath.inf))
print("isnan(x) function :- ", cmath.isnan(cmath.nan))

print("-------- CONSTANTS ---------")

# Constants 
print("e :- ", cmath.e)
print("inf :- ", cmath.inf)
print("infj :- ", cmath.infj)
print("nan :- ", cmath.nan)
print("nanj :- ", cmath.nanj)
print("pi :- ", cmath.pi)
print("tau :- ", cmath.tau)
